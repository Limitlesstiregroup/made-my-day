#!/usr/bin/env node
const fs = require('node:fs');
const path = require('node:path');
const crypto = require('node:crypto');

function argValue(name) {
  const prefix = `--${name}=`;
  const found = process.argv.find((arg) => arg.startsWith(prefix));
  return found ? found.slice(prefix.length).trim() : '';
}

const oncall = argValue('oncall') || 'ops-oncall';
const escalationUrl = argValue('escalation-url') || 'https://github.com/Limitlesstiregroup/made-my-day/blob/main/docs/ESCALATION.md';
const enforceLiveReady = argValue('enforce-live-ready') || '1';

const repoRoot = path.resolve(__dirname, '..');
const secretDir = path.join(repoRoot, '.secrets');
const adminTokenFile = path.join(secretDir, 'made-my-day-admin-token');
const envFile = path.join(repoRoot, '.env.ga.local');

fs.mkdirSync(secretDir, { recursive: true, mode: 0o700 });

if (!fs.existsSync(adminTokenFile)) {
  const token = crypto.randomBytes(24).toString('base64url');
  fs.writeFileSync(adminTokenFile, `${token}\n`, { mode: 0o600 });
} else {
  fs.chmodSync(adminTokenFile, 0o600);
}

const envContents = [
  '# Auto-generated by scripts/ga-quickstart.js',
  '# Usage: set -a; . ./.env.ga.local; set +a',
  `MADE_MY_DAY_ADMIN_TOKEN_FILE=${adminTokenFile}`,
  `MADE_MY_DAY_ONCALL_PRIMARY=${oncall}`,
  `MADE_MY_DAY_ESCALATION_DOC_URL=${escalationUrl}`,
  `MADE_MY_DAY_ENFORCE_LIVE_READY=${enforceLiveReady}`,
  ''
].join('\n');

fs.writeFileSync(envFile, envContents, 'utf8');

console.log(`Wrote ${envFile}`);
console.log(`Wrote ${adminTokenFile}`);
console.log('Next: set -a; . ./.env.ga.local; set +a; npm run release:check');
